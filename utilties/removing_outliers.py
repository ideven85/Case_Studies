import warnings

import numpy as np


def removing_outliers_using_std(arr):
    mu = np.mean(arr)
    std = np.std(arr)
    out = [x for x in arr if (x > mu - 2 * std)]
    # print(out)
    out = [x for x in out if (x < mu + 2 * std)]
    return out


